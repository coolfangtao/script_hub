import streamlit as st

def show_changelog():
    """
    在页面上渲染“最近更新”这个UI组件。
    未来所有更新日志的修改，都只在这个函数里进行。
    """
    st.header("📝 最近更新 (Changelog)")
    with st.expander("点击查看更新历史", expanded=True):
        # 你可以在这里用Markdown语法自由地添加新的更新记录
        st.markdown("""
            - **v1.4 (2025-10-22):**
                - 🗓️️ 新增每日任务看板功能
            - **v1.3 (2025-10-20):**
                - ✍️ 新增用户反馈区，基金盯盘等功能
            - **v1.2 (2025-10-19):**
                - 🚀 主页全新改版，提供更清晰的指引。
                - 🖼️ 图像处理模块新增“图片翻译”功能。
                - 🐞 修复了文本对比在处理长文本时可能出现的卡顿问题。
            - **v1.1 (2025-10-18):**
                - 📊 新增“根据ASIN关键词结果自动生成Listing标题”的实验性功能。
            - **v1.0 (2025-10-18):**
                - 🎉 脚本中心首次上线！
        """)